var path_file_collection = "http://localhost:5000/uploads/"

type handlersCollection struct {
	CollectionRepository repositories.CollectionRepository
}

func HandlerCollection(CollectionRepository repositories.CollectionRepository) *handlersCollection {
	return &handlersCollection{CollectionRepository}
}

func (h *handlersCollection) FindCollections(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Content-Type", "application/json")

	collections, err := h.CollectionRepository.FindCollections()
	if err != nil {
		w.WriteHeader(http.StatusInternalServerError)
		response := dto.ErrorResult{Code: http.StatusBadRequest, Message: err.Error()}
		json.NewEncoder(w).Encode(response)
	}

	w.WriteHeader(http.StatusOK)
	response := dto.SuccessResult{Code: http.StatusOK, Data: collections}
	json.NewEncoder(w).Encode(response)
}

func (h *handlersCollection) GetCollection(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Content-Type", "application/json")

	id, _ := strconv.Atoi(mux.Vars(r)["id"])

	collection, err := h.CollectionRepository.GetCollection(id)
	if err != nil {
		w.WriteHeader(http.StatusInternalServerError)
		response := dto.ErrorResult{Code: http.StatusBadRequest, Message: err.Error()}
		json.NewEncoder(w).Encode(response)
	}

	w.WriteHeader(http.StatusOK)
	response := dto.SuccessResult{Code: http.StatusOK, Data: collection}
	json.NewEncoder(w).Encode(response)
}

func (h *handlersCollection) CreateCollection(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Content-Type", "application/json")

	userInfo := r.Context().Value("userInfo").(jwt.MapClaims)
	id := int(userInfo["id"].(float64))

	request := new(collectiondto.CreateCollection)
	if err := json.NewDecoder(r.Body).Decode(&request); err != nil {
		w.WriteHeader(http.StatusBadRequest)
		response := dto.ErrorResult{Code: http.StatusBadRequest, Message: err.Error()}
		json.NewEncoder(w).Encode(response)
	}

	validate := validator.New()
	err := validate.Struct(request)
	if err != nil {
		w.WriteHeader(http.StatusBadRequest)
		response := dto.ErrorResult{Code: http.StatusBadRequest, Message: err.Error()}
		json.NewEncoder(w).Encode(response)
		return
	}

	collection := models.Collection{
		LiteraturID: request.LiteraturID,
		Literatur:   models.LiteraturResponse{},
		UserID:      id,
		User:        models.UsersProfileResponse{},
	}

	data, err := h.CollectionRepository.CreateCollection(collection)
	if err != nil {
		w.WriteHeader(http.StatusInternalServerError)
		response := dto.ErrorResult{Code: http.StatusBadRequest, Message: err.Error()}
		json.NewEncoder(w).Encode(response)
	}

	w.WriteHeader(http.StatusOK)
	response := dto.SuccessResult{Code: http.StatusOK, Data: data}
	json.NewEncoder(w).Encode(response)
}

